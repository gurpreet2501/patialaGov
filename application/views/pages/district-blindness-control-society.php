<p class="heading">Main Activities of DBCS Patiala </p>
<p align="justify"><b> a)	Cataract Blindness :</b> DBCS Patiala take the following steps to control Cataract Blindness by :</p><p>
</p><ol><li>
	Identifying the blind and list them in village registers </li>
<li>	Organize screening camps for confirming the cataract blind for referral </li>
<li>	Transport the cataract blind to the base hospital/camp </li>
<li>	Free Surgery at base hospital/camp</li>
<li align="justify">	Follow-up of operated cases, carrying out refraction and providing best corrective spectacles.</li></ol>
<p align="justify"><b>DBCS Provide Grant-in-aid for free cataract operations by Voluntary organization / PRI etc. in camps/ fixed facilities from Rs. 200/- to 
Rs. 750/- as per the guidelines of Govt. of India. </b></p>  
<p align="justify"><b><u>More than 10000 cataracts operations are done every year in Patiala.</u></b></p> 
<p><b>b)	School Eye Screening Programme </b></p>
	<p align="justify">5 to 7% of children aged 10-14 in Patiala Distt. have problems with their eye sight affecting their participation and learning at school. This can be easily corrected by a pair of spectacles.</p>
<p class="heading">District Blindness Control Society Patiala on annual basis </p>
<ul><li>
	Screen all children in the age group of 10 to 14 years for refractive errors (difficulty in clearly seeing distant or near objects);</li>
<li>	Train teachers, preferably with science background, females and those wearing spectacles, identifying children with eye problems;</li>
<li>	Refer children with suspected refractive errors, to the Ophthalmic Assistant at the PHC for refraction ;</li>
<li>	Prescription of spectacles ;</li>
<li>	Provide children from poor families with free glasses ;</li></ul>
<p align="justify"><b><u>48000 to 50000 school children are screened every year and more than 1000 free spectacles are distributed to poor and needy students.</u></b></p>   

<p><b>c)	Training to Link Workers </b></p>
<p align="justify">DBCS Patiala trains health workers and one link worker in a village for mobilizing and ensuring :</p>
<ul>
<li>	Preparation of village-wise blind registry through local bodies like Panchayats, Mahila Mandals and community groups, Health workers, teachers, anganwadi workers, youth clubs, volunteers etc.;</li>
<li>	Motivation and transportation of cases through grass root NGOs, peripheral health staff, link workers and village community;</li>
<li>	Training in interpersonal communication through village level link workers, satisfied patients and grief counsellors in hospitals;</li>
<li>	Public awareness through various media channels.</li></ul>
<p><b>d)	Eye donation and Corneal Blindness </b></p>
<ul>
<li>	DBCS Patiala Organizes Eye donation fortnight from 25th August to 8th September every year to promote eye donation/ eye banking.</li>
<li>	Corneal blindness mainly occurs among children and young adults;</li>
<li>	Estimated 40 thousand new cases occur every year ;</li>
<li>	Common causes include Vitamin-A deficiency, eye infections and injuries;</li>
<li>	Only treatment available is corneal transplantation;</li></ul>
