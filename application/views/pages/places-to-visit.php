			<a name="Tourist Attractions"></a>
			<p class="Heading">Tourist Attractions in Patiala</p>
			<p align="justify"><b>Qila Mubarak Complex</b></p>
			<p align="justify"><img src="../images/Image45.gif" align="right" alt="Qila Mubarak" width="300" height="180">
			<b>T</b>he Qila Mubarak complex stands in 10-acre ground in the heart of the city, and contains the main palace or Qila Androon (literally,'inner fort'), the guesthouse or Ran Baas and the Darbar Hall. Outside the Qila are the Darshani Gate, a Shiva temple, and bazaar shops which border the streets that run around the Qila and sell precious ornaments, colorful hand-woven fabrics, ‘jootis’ and bright ‘Parandis’.</p>
			
			<p align="justify"><b>Qila Androon</b></p>
			<p align="justify"><b>T</b>he entrance is through an imposing gate. The architectural style of this palace is a synthesis of late Mughal and Rajasthani. The complex has 10 courtyards along the north - south axis and each courtyard is unique in size and character, some being broad, others very small and still others mere slits in the fabric of building.Though the Androon is a single interconnected building, it is spoken of as a series of palaces. Each set of rooms makes a cluster around a courtyard, and each carries a name: Sheesh Mahal, Toshakhana, Jalau Khana, Chand Mahal, Rang Mahal, Treasury and Prison. Ten of the rooms are painted with frescoes, or decorated intricately with mirror and gilt. In a tiny portion of the complex is a little British construction with Gothic arches, fire places made of marble and built-in toilets perched on the Mughal Rajasthani roof!. Burj Baba Ala Singh even today has a fire smoldering ever since&nbsp;the time of Baba Ala Singh, along with a flame brought by him from Jwalaji. </p>
			
			<p align="justify"><b>Rang Mahal and Sheesh Mahal</b></p>
			<p align="justify"><img src="../images/rang_sheesh_mahal.gif" alt="Rang Mahal and Sheesh Mahal" align="left" width="281" height="202"><b>T</b>he two mahals contain a large no. of frescoes, most of which were made under Maharaja Narender Singh. Within the Qila Mubarak are 16 painted and mirror-worked chambers. For instance, the Darbar room is illustrated with Vishnu avatars and stories of courage or generosity, the ladies' chamber with illustrations from famous romantic epics, and two other chambers with illustrations of the qualities of a good or bad king. The frescoes, among the finest painted in India in the second half of the 19th century are evidently the work of artists from Rajasthani, Pahari and Avadhi traditions. </p>
			
			
			<p align="justify"><b>Ran-Baas</b></p>
			<p align="justify"><b>T</b>his building was probably a guest house. It has an imposing gateway and two courtyards, both with fountains and small tanks. A room in the first courtyard-with painted walls and a gilt throne-was probably for semi -formal audience. A few pavilions are set among painted walls on the upper storey. Facing each other across the courtyard are two exquisite chambers, one painted and the other decorated with mirror work.</p>
			
			<p align="justify"><b>Darbar Hall (Divan Khana)</b></p> 
			<p align="justify"><img src="../images/Image53.gif" align="right" alt="Darbar Hall (Divan Khana)" width="162" height="150"><b>U</b>sed for large audiences and important public occasions, the Darbar has been converted into a museum displaying dazzling chandeliers and armor, including the sword and dagger of Guru Gobind Singh and Nadir Shah's sword. The hall was built on a high plinth over a network of tunnels which were service conduits. The facade gives the impression of a double-storey building, with 'upper storey' windows and a balcony at the first floor level, but the delicately worked wood-and-glass doors open into a huge 15m-high chamber. At the far end is a raised platform, where the Maharaja sat . The wooden frame work of the ceiling holds decorated Plaster-of Paris tiles painted in Arabic style and the ceiling is hung with a fabled collection of chandeliers. </p>
			
			<p align="justify"><b>Jalau Khana and Sard Khana (Cool Room)</b></p>
			<p align="justify"><b>B</b>oth were much later constructions. The Jalau Khana is a small, two storeyed building with a central hall in late Colonial style, where regalia were displayed. The Sard Khan provided an escape from the summer heat. A deep well inside it acted as a wind tunnel, bringing cool air into the ground-floor rooms and the basement. Outside, there is a formal garden with waterways and fountains. </p>
			
			<p align="justify"><b>Lassi Khana (Kitchen)</b></p>
			<p align="justify"><b>A</b>nother small, two-storeyed building with a central courtyard and a well.It adjoins the Ran-bass, and a passage links it to the Qila Androon. Local residents say that at one time this kitchen had the capacity to serve nearly 35,000 people every day, but following an economy drive, the Lassi Khana restricted itself to serving only a modest 5,000 people.</p>
			
			<p align="justify"><b>Shahi Samadhan</b></p>
			<p align="justify"><b>T</b>he Samadhan, where Maharaja Rajinder Singh once built a garden, now holds cenotaphs of erstwhile rulers, looked after by a mahant. </p>
			
			<p align="justify"><b>Moti Bagh Palace</b></p>
			<p align="justify">
			<b>S</b>tarted during the reign of Maharaja Narinder Singh, <img src="../images/Image54.gif" alt="Moti Bagh Palace" align="left" width="286" height="187">it was completed under Maharaja Bhupinder Singh in the early 20th century. The Old Moti Bagh Palace now houses the National Institute for Sports. The facade has Rajasthan-style jharokas and chhatris, and the palace is set in a beautiful garden with terraces, water channels and a Sheesh Mahal.</p>
			
			<p align="justify"><b>Sheesh Mahal</b></p>
			
			<p align="justify">
			<b>T</b>he Sheesh Mahal was built <img src="../images/seesh-mehal.gif" alt="Sheesh Mahal" width="300" height="180" align="right">behind the main Moti Bagh Palace to serve as a pleasure complex.The paintings in two of its well maintained , mirror-worked chambers are of Kangra and Rajasthani qalam, depicting the poetic visions of Keshav, Surdas and Bihari. The Sheesh Mahal now houses a museum, an art gallery, the famed medal gallery and also the North Zone Cultural Centre.</p>
			
			<p align="justify"><b>Lachman Jhoola</b></p> 
			<p align="justify"><b>A</b>cross <img src="../images/Image55.jpg" border="0" alt="Lachman Jhoola" width="300" height="200" align="left">the small Lake in front of Sheesh Mahal is a magnificent suspension bridge which being a replica of the famous Lakshman Jhoola at Rishikesh, is also named as Lachman Jhoola. It links the Sheesh Mahal with the Banasar Ghar on the other side of the lake. The Banasar Ghar now houses the North Zone Cultural Center and a hall for setting up exhibitions.</p>
			
			<p align="justify"><b>Bir Moti Bagh</b></p>
			<p align="justify"><b>A</b> 1,600-acre forest on the outskirts of Patiala, The Bir was originally the hunting preserve of the Maharaja. Most of the Bir is still forest, but parts of it house a zoo and a deer park, as well as a pilot project on medicinal plants.</p>
			
			<p align="justify"><b>Mall Road</b></p>
			<p align="justify"><b>B</b>aradari is the colonial area of Patiala. On one side of Mall road is the Baradari, and on the other is the walled city. All along the vibrant Mall Road are fountains and beautiful paved walkways, as well as goverment offices (all buildings conforming to one architectural style), entertainment spots, including cinema theatres and the Rajendra tank, and temples. (The Rajendra Tank is actually a large lake which once attracted migratory birds in winters. Boating facilities are available here.)</p>
			
			<p align="justify"><b>Rajindera Kothi</b></p>			
			<p align="justify"><img src="../images/Image56.gif" alt="Rajindera Kothi" width="200" height="100" align="right"></p>
			<p align="justify"><b>R</b>ajindera Kothi: Set in the heart of the Baradari Gardens, this late 19th Century Palace built in colonial style by Maharaja Rajindra Singh till recently housed Punjab States Archives. PUDA is planning to exploit this building as a potential Heritage Hotel.</p>
			
			<p align="justify"><b>Baradari Gardens</b></p>
			<p align="justify"><img src="../images/baradari_gardens.gif" alt="Baradari Gardens" width="200" height="100" align="left"><b>T</b>he Baradari gardens surround the Baradari palace located in the north of old Patiala city, just outside Sheranwala Gate. The gardens, laid under Maharaja Rajindera Singh were planted extensively rare trees and shrubs, dotted with impressive Colonial buildings and a marble statue of Maharaja Rajindera Singh and the Fern House. The 19th century Fern House, a replica of the one in Calcutta forms a unique attraction along with quaint Rink Hall. </p>
			
			<p align="justify"><b>Ijlas-e Khas</b></p>
			<p align="justify"><img src="../images/ijlas.gif" alt="Ijlas-e Khas" width="250" height="110" align="right"><b>I</b>ntended to be the Administrative Secretariat of the princely state, this beautiful building now houses the offices of the Punjab State Electricity Board.</p>
			<br><br>
			
			<p align="justify"><b>Gurudwara Dukhniwaran Sahib</b></p>
			<p align="justify"><img src="../images/Image57.gif" align="left" alt="Gurudwara Dukhniwaran Sahib(Aerial View)" width="250" height="150"> <b>T</b>he villagers of Lehal donated land for the modest  Gurudwara built on this elevated site, said to have been visited by Guru Teg Bahadur. The legend is that anyone who prays at this Gurudwara is relieved of his suffering ('dukhniwaran'). A new bigger building is now being constructed.</p>
			
			<p align="justify"><b>Kali Temple</b></p>
			<p align="justify"><b>M</b>aharaja Bhupinder Singh was inspired to build this temple and bring the 6-ft statue of Kali from Bengal to Patiala. This large complex attracts devotees, Hindu and Sikh, from distant places. A much older temple of Raj Rajeshwari is also situated in the center of this complex.</p>
			
			<p align="justify"><b>Qila Bahadurgarh</b></p>
			<p align="justify"><b>T</b>he ninth Guru Tegh Bahadur stayed at Saifabad during his travels. Originally known as Saifabad, it was renamed Bahadurgarh by Maharaja Amar Singh who reinforced and renovated it.&nbsp; The present fort dates back to Maharaja Karam Singh. He built a beautiful Gurudwara on the Patiala-Rajpura road (6 Km from Patiala City).</p>
			
			<p align="justify"><b>Panj Bali Gurdwara</b></p>
			<p align="justify"><b>N</b>awab Saif Khan, an admirer of Guru Teg Bahadur, Commemorated the guru's visit by building two gurudwaras, one inside the fort and the other across the road, now known as Panch Bali Gurudwara.&nbsp; </p>
			<p align="justify"><b>Maiji Di Sarai</b></p>
			<p align="justify"><b>T</b>his monument in the old city now houses the criminal investigation agency office.</p>
			
			<a name="Historical Places"></a>
			<p class="Heading">Historical Places Surrounding Patiala</p>

			<p align="justify"><b>Banur</b></p>
			<p align="justify"><b>S</b>ituated on the Sukhna Nadi, a tributary of the Ghaggar, at a distance of nine miles north-east of Rajpura on the Rajpura-Chandigarh Road, Banur is an ancient town. Its ruins testify to its former grandeur and importance, but its history has been lost in oblivion. Its ancient name was Pushpa or Popa Nagri or Pushpawati-the City of Flowers, and it was famous for the scent of <i>chambeli </i>flowers grown in its numerous gardens. The place was also well known for its musicians. One Banno <i>Chhimban</i>, a washer woman, is mentioned as a great musician of the days of Akbar.</p>
			
			<p align="justify">During the reign of Emperor Akbar, Banur became a Mahal of the Sarkar of  Sirhind and continued to be so up to the beginning of the eighteenth century. </p>
			
			<p align="justify">In addition to the tomb of Malik Suleman, the suburbs of Banur contain the ruins of an old imperial fort, popularly known as Zulmgarh, the citadel of tyranny, and of another fort of Banda Ali Beg of a more recent date.</p>
			
			<p align="justify"><b>Samana</b></p>
			<p align="justify"><b>S</b>amana at a distance of 17 miles south-west of Patiala is a place of considerable antiquity. It traces its history to the days of Raja Jaipal who ruled over, among others, the territories of Bhatinda, Samana. It fell into the hands of Shahab-ud-Din Muhammad Gauri after the conquest of Ajmer and Delhi and was entrusted to Qutb-ud-Din Aibek in 1192, along with the territories of Ghuram and Sunam. With the increasing importance of Sirhind under the Mughals, Samana received a little set-back.</p>
			<p align="justify">While Samana is said to be a place of saints and scholars during the Mughal days, it is notorious also for its professional executioners, who served at Delhi and Sirhind. Sayyad Jala-ud-Din, who executed Guru Teg Bahadur at Delhi in 1675 was from Samana. Beg brothers, who mercilessly butchered the younger sons of Guru Gobind Singh also belonged to Samana. This hated town was therefore one of the first places to have been sacked by Banda Bahadur. But the Mughals were yet too strong for the rising power of Sikhs and Samana had to be given up by them towards the end of 1710 AD.It was retaken in about 1742 AD by Baba Ala Singh, the founder of the Patiala ruling family and was recognized as a part of his territories by Ahamd Shah Durani.</p>
			
			<p align="justify"><b>Sanaur</b></p>
			<p align="justify"><b>T</b>he Town Sanour lies 4 miles South-east of Patiala. It lies on a high mound. The town is of some antiquity. In the time of Babar, Malik Baha-ud-Din Khokar became the chief of this pargana which was called Chaurasi, having 84 villages. In 1748, it came into the possession of Baba Ala Singh.</p>
			
			<p align="justify"><b>Ghuram (Kuhram or Kahram)</b></p>
			<p align="justify"><b>S</b>ituated in 30°
			 7' N and 76° 33' E 29 miles (slightly West) of Rajpura and 6 miles South (slightly East) of Patiala. Ghuram (Renamed Ramgarh) is a very ancient palace. An old tradition takes it back to the days of the <i>Ramayana, </i>being the abode of Rama’s maternal grandfather. The old ruins in its vicinity speak for its antiquity, though its early history has been long lost. During the days of Rajput Kings, Ghuram (Kuhram of the Persian Writers) was an important town with a strong fort to protect it.</p>
			 
			<p align="justify">During the fifties of the seventeenth century, Ghuram was held by Malhi Khan as a biswedar proprietor. He was a tyrant and was notorious for his extortions. Baba Ala Singh of Patiala had risen to eminence by now. He was a brave soldier and humane ruler and was looked upon by the oppressed people as source of timely help and consolation. The people of Ghuram came to Patiala and appealed to his noble wife, Mai Fato, for deliverance. Malhi Khan was dispossessed of Ghuram and it was taken under the direct control of Patiala. Maharaja Karam Singh of Patiala built a fort here and named it Ramgarh, evidently in memory of Rama of the <i>Ramayana</i> fame.</p>
			
