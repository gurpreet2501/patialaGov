                            <p><b>P</b>atiala&nbsp;has been
                            cultural and academic center of
                            northern India. The erstwhile rulers
                            of Patiala state were great patrons
                            of art and culture. Thus under the
                            Maharajas, Patiala had developed into
                            an important educational centre. It
                            was the first city in this part of
                            the country to have a Degree College-
                            the Mohindra College- established in
                            1875. This city has the credit of
                            setting up the first printing press
                            in Punjab – the famous Printing
                            Press of Munshi Naval Kishore and for
                            manufacturing the first Punjabi
                            Typewriter.&nbsp; </p>
                            <p>Today, Patiala is an important
                            seat of learning with a full-fledged
                            University, a Government Medical
                            College, a Dental College, an
