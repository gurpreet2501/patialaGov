                            <p align="justify">Tuberculosis (TB) is an infectious
                            disease caused by Mycobacterium
                            tuberculosis. It is spread through
                            the air by a person suffering from
                            pulmonary TB. A single Sputum +ive
                            Patient can infect 10-15 people in a
                            year if not treated. India contribute
                            1/3rd global burden of TB and TB has
                            been declared a Global Emergency.</p>
                            <p align="justify">In India there are 14 million TB
                            patients, 2 million new TB patients
                            are added every year of which 1
                            million are of infectious type.</p><p align="justify"> More
                            adults die from TB than from any
                            other infectious disease in India,
                            more than 1000 every day and 1 every
                            minute. TB kills women more than all
                            causes of maternal mortality combined
                            and every year in India 300000
                            children leave school on account of
                            their parents TB.<b> TB is the most
                            common opportunistic disease in
                            people living with HIV.</b></p>
<p align="justify">TB is major
                            barrier to economic development
                            costing India Rs. 12,000 crore a
                            year. RNTCP has covered almost whole
                            of the country’s population. It
                            has saved about 5 Lac lives in India
                            since its implementation.</p>
                            <p align="justify">Directly Observed Treatment, Short
                            course (DOTS) is a comprehensive and
                            cost effective strategy for TB
                            control. This strategy has proven
                            effective in controlling TB on a mass
                            basis.</p>
                            <p align="justify"><b>In Punjab pilot project was
                            launched in 2001 in district Patiala
                            on World TB Day ie. March 24th 2001.</b>
                            RNTCP is being implemented through
                            Distt. TB Control Society. <b>Investigations
                            like Sputum Examination is done free
                            of cost at all the Microscopy
                            Centres(MC).</b> Any Chest
                            Symptomatic case having cough more
                            then 3 weeks is referred for sputum
                            examination to nearest MC. Each MC
                            covers a population of 1lakh. To a TB
                            Patient full course of medicines
                            under DOTS is given free of cost.
                            Medicines are given by the DOTS
                            provider under his/her direct
                            observation on every alternate day in
                            the DOT center nearby to the
                            residence of the patient. Complete
                            record of the patient’s
                            treatment is kept for further
                            quarterly analysis.</p>
                            <p align="justify">In Patiala there are 18 Microscopy
                            Centres, 4 TB Units &amp; 889 DOTS
                            centres. Almost all Medical Officers,
                            659 DOTS Providers, 46 Lab.
                            Technicians and contractual staff
                            under RNTCP which includes one MO,
                            STS, STLS, LT &amp; DEO have been
                            imparted training. In August 2002 a
                            seminar was held in Govt. Rajindra
                            Hospital Patiala in which Private
                            Practitioners of IMA were Involved
                            and in Oct,2002 a CME was held
                            involving faculty of Govt. Medical
                            College Patiala. In May2005 a
                            conference of RNTCP was held in Govt.
                            Medical College Patiala in which
                            faculty of Medical Colleges of
                            Punjab, DTO’s, MOTC’s were
                            the participants. Since August 2005
                            TB Awareness camps are being held in
                            Distt. Patiala particularly in SLUM
                            Areas.</p>
                            <p align="justify">During 2005 total of <b>952 DOTS
                            centers (Urban - 825 &amp; Rural -
                            127)</b> are involved in RNTCP where
                            Anganwari workers Private
                            Practitioners and paramedical staff
