<p class="PageHeading" id="item3"><u><b>Sporting Venues</b></u></p>
<p class="Heading" align="justify"><b><u>NSNIS ,Patiala</u></b></p>
<p align="justify"><img src="../images/NIS.jpg" border="0" alt="NIS" width="300" height="200" align="right">Netaji Subhas National Institute of Sports, Patiala India which is also Asia's largest Sports Institute is housed in the palatial monumental building and sprawling lawns built by the erstwhile Maharaja of Patiala whose descendants dedicated this complex for the promotion of sports to the people of India.

This Institute is popularly known as the "Mecca" of Indian Sports and has produced Coaches of high caliber and significantly contributed in rendering their expertise and assistance in the preparation of the national teams for various International competitions. Housed in the erstwhile Palace of Maharaja (King) of Patiala (Punjab), National Institute of Sports, which is now the Academic Wing of the Sports Authority of India was established by the Government of India on 7th May, 1961 with the objective of developing sports in the country on Scientific lines and to train the Coaches in different sports disciplines. The Institute was renamed as Netaji Subhas National Institute of Sports (NSNIS) on 23rd January, 1973.

The Institute is housed in an area of 268 acres. The Institute has enormous facilities for academic programmes &amp; sports training and 4 hostels for the comfortable stay of the students, national players and coaches. The Institute is having 115 rooms which houses the office, library, museum, class rooms &amp; staff club. One Guest House with lounge &amp; conference hall is situated in the Institutes green belt. Department of Sports Medicine is providing all medical assistance to the players, students and staff. The sports training facilities are of international standard comprising of Gymnasium, Swimming Pool, Indoor Halls, Cycling Velodrome, Squash Courts, Conditioning Units, Hockey Fields (Grass &amp; synthetic), Athletic Track (Cinder &amp; synthetic) &amp; Out Door Courts. For the recovery of players well maintained Sauna Bath, Steam Bath and Hydrotherapy facilities are available.
