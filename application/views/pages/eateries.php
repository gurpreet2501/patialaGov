<p class="heading" align="justify"><b><u>Maharani Club</u></b></p>
<p align="justify"><img src="../images/club.jpg" border="0" alt="Maharani club" width="300" height="200" align="right"><b>T</b>he House of Patiala was well known for its patronage of the royal game of cricket. The rulers themselves were outstanding cricketers and great lovers of the game. They brought Patiala on the world map of cricket and developed it to be nursery of keen and energetic cricketers.
It was Maharaja Rajindra Singh who was a pioneer among the Indian princes to take the game in the right earnest. Although only 14 years of age the young Maharaja showed great zeal and aptitude for the game of cricket. The Maharaja and his young brother, Raja Ranbir Singh use to take up there positions as to rival captions on the joyous field, eager to inflict defeat on the other.
The ground on which Maharaja Rajindra Singh used to play matches was very close to the Rajindra Palace. After the completion of the Cricket Pavillion, Maharaja Rajindra Singh, established a regular club of Cricket and named it ‘PATIALA CRICKET CLUB’.
The activities of the cricket club were looked after by Col. K.M.Mistri and other prominent coaches. In 1920, more sports were added in the club activities. In 1923, the Cricket Club’s name was changed into R.G.C.C. (Rajindra Gymkhana Cricket Club).
During Maharaja Bhupindra Singh’s reign many foreign teams visited India and the ruler saw to it that they played a match in Baradari Grounds. It was only in this magnificent ground that many of the visiting foreign teams met their waterloo. He was also acclaimed as British Empire’s leading cricketer in 1911.
After the creation of PEPSU in 1956, when Maharaja Yadavindra Singh was made the Raj Parmukh of PEPSU, this building was converted into Rajindra Gymkhana &amp; Mahindra Club under the stewardship of Royal family of Patiala, which is still carried on. The games like table tennis, badminton, swimming pool is available for the members of the club.</p>
